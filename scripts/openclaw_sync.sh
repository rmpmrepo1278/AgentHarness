- User asks about system health → run validate.sh
- After deploying anything new → run service_registry.sh then openclaw_sync.sh
SKILL

    log_ok "Meta-skill: ${meta_dir}/SKILL.md"
}

# =============================================================================
# Main
# =============================================================================
main() {
    log_header "OpenClaw Sync"

    # Ensure registry is fresh
    if [ ! -f "${REGISTRY}" ] || \
       [ "$(find "${REGISTRY}" -mmin +30 2>/dev/null)" ]; then
        log_info "Registry stale or missing. Running service discovery..."
        bash "${SCRIPT_DIR}/service_registry.sh"
    fi

    # Skills directory must exist (discovered by load_openclaw_paths)
    if [ ! -d "${SKILLS_DIR}" ]; then
        log_warn "Skills directory not found at ${SKILLS_DIR}"
        log_info "Creating it..."
        mkdir -p "${SKILLS_DIR}"
    fi

    # Generate skills for each service with capabilities
    local skills_generated=0

    python3 -c "
import json
reg = json.load(open('${REGISTRY}'))
for svc in reg['services']:
    if svc.get('chaguli_capabilities') or svc.get('endpoints'):
        print(json.dumps(svc))
" 2>/dev/null | while IFS= read -r service_json; do
        generate_skill "${service_json}"
        ((skills_generated++))
    done

    # Generate meta-skill for AgentHarness itself
    generate_meta_skill

    # Update AGENTS.md (append/update managed section, preserve existing content)
    generate_agents_section

    # Summary
    local total_skills
    total_skills=$(find "${SKILLS_DIR}" -name "SKILL.md" 2>/dev/null | wc -l)

    log_header "Sync Complete"
    echo ""
    echo "  Skills generated: ${total_skills}"
    echo "  Skills directory: ${SKILLS_DIR}/"
    echo "  AGENTS.md: ${AGENTS_FILE:-not configured}"
    echo ""
    echo "  OpenClaw hot-reloads skills — Chaguli has the new capabilities now."
    echo ""

    # List generated skills
    if command -v openclaw &>/dev/null; then
        log_info "Installed skills:"
        openclaw skills list 2>/dev/null || find "${SKILLS_DIR}" -name "SKILL.md" -exec dirname {} \; | xargs -I{} basename {}
    else
        log_info "Skills:"
        find "${SKILLS_DIR}" -name "SKILL.md" -exec dirname {} \; | xargs -I{} basename {} | sort
    fi
}

main "$@"
